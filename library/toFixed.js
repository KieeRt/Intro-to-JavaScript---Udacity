Info: "https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Global_Objects/Number/toFixed"
//Syntax:
 varibles.toFixed([digits])
//A string representing the given number using fixed-point notation.
//Examples:
 var balance = 325;
 balance.toFixed(2)
//  325.00.
//----------------
var balance = 325.651;
balance.toFixed(1);
// 325.7

var balance = 325.650;
balance.toFixed(1)
//325.6
